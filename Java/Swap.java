/*First using a third variable*/
import java.io.*;
import java.util.*;
public class swap
{
  public static void main()
  {
    int a,b,c;
    System.out.println("Numbers before swap"+a+""+b);
    c=a;
    a=b;
    b=c;
    
    System.out.println("Numbers after swap"+a+""+b);
  }
}
    
